const spanishTimeZones = new Set(["Europe/Madrid", "Atlantic/Canary"]);
const spanishLocales = new Set(["es-es", "ca-es", "eu-es", "gl-es"]);

function setLanguage(lang, persist = false) {
    const nextLang = lang === "en" ? "en" : "es";
    const dictionary = translations[nextLang];

    document.documentElement.lang = nextLang;
    document.title = dictionary.metaTitle;

    const description = document.querySelector('meta[name="description"]');
    if (description) {
        description.setAttribute("content", dictionary.metaDescription);
    }

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.getAttribute("data-i18n");
        if (dictionary[key]) {
            element.textContent = dictionary[key];
        }
    });

    document.querySelectorAll("[data-set-lang]").forEach((button) => {
        button.setAttribute("aria-pressed", String(button.dataset.setLang === nextLang));
    });

    if (persist) {
        localStorage.setItem("jtm-language", nextLang);
    }
}

function browserLooksSpanish() {
    const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone;
    const languages = navigator.languages?.length ? navigator.languages : [navigator.language];
    const hasSpanishLocale = languages
        .filter(Boolean)
        .map((language) => language.toLowerCase())
        .some((language) => spanishLocales.has(language));

    return spanishTimeZones.has(timeZone) || hasSpanishLocale;
}

async function detectLanguage() {
    const savedLanguage = localStorage.getItem("jtm-language");
    if (savedLanguage) {
        setLanguage(savedLanguage);
        return;
    }

    const fallback = browserLooksSpanish() ? "es" : "en";
    setLanguage(fallback);

    const controller = new AbortController();
    const timeout = window.setTimeout(() => controller.abort(), 1200);

    try {
        const response = await fetch("https://www.cloudflare.com/cdn-cgi/trace", {
            cache: "no-store",
            signal: controller.signal
        });
        const trace = await response.text();
        const country = trace
            .split("\n")
            .find((line) => line.startsWith("loc="))
            ?.replace("loc=", "")
            .trim();

        if (country) {
            setLanguage(country === "ES" ? "es" : "en");
        }
    } catch (error) {
        setLanguage(fallback);
    } finally {
        window.clearTimeout(timeout);
    }
}

document.querySelectorAll("[data-set-lang]").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.setLang, true));
});

